class Product
{
  final int id;
  final String name;
  final String category;
  final String price;
  final String image;
  final String description;
  int quantity;

Product({
  required this.id,
  required this.name,
  required this.category,
  required this.price,
  required this.image,
  required this.description,
  required this.quantity,
});
}